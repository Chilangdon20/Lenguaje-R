#Redondeo & Decimales

#print(x,n)-Muestra las n cifras significativas del numero x
print(sqrt(2),10)
#round(x,n)-Redondea a n cifras significativas un resultado o vector nuemerico
round(39.3483,2)
#floor(x)-Mayor entero por defecto de c
floor(3.5)
#ceiling(x)- Mayor entero por parte de exceso de x
ceiling(3.567)
#trunc(x)- parte entera de x, eliminando la parte decimal
trunc(2.455)
